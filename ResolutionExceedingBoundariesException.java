package ascii_art;

/**
 * The ResolutionExceedingBoundariesException class represents an exception that is thrown when the resolution
 * of the ASCII art exceeds the boundaries of its valid range.
 */
public class ResolutionExceedingBoundariesException extends Exception {

    /**
     * Constructs a ResolutionExceedingBoundariesException with no detail message.
     */
    public ResolutionExceedingBoundariesException() {
        super();
    }
}
